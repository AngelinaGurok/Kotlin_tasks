 * Поменять порядок цифр заданного числа n на обратный: 13478 -> 87431.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun revert(n: Int): Int {
    var temp = n
    var  result = 0
    while (temp != 0){
        result = result * 10 + temp % 10
        temp /= 10
    }
    return result
}

